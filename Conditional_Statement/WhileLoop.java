package DSA_Programming.Conditional_Statement;

public class WhileLoop {
    public static void main(String[] args) {
        int counter = 0;
        while (counter < 100) {
            System.out.println("Hello Mr.Dev Srivatav");
            counter ++;
        }
        System.out.println("Printed Hello Mr.Dev Srivatav 100x");
    }
}
